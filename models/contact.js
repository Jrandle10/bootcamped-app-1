import mongoose from 'mongoose'

const Schema = mongoose.Schema

const contactSchema = new Schema({
  name: { 
    type: String,
    required: true
  },
  title: {
    type: String,
  }, 
  email: {
    type: String
  },
  linkedin: {
    type: String
  },
  gaGrad: {
    type: Boolean,
    default: false
  },
  user:{type: Schema.Types.ObjectId, ref:"User"}
})

const Contact = mongoose.model('Contact', contactSchema)

export { Contact }